# Ensure child elements exist (uses parent's OwnerDocument)
function Ensure-Element {
    param (
        $parent,
        [string]$name
    )
    $node = $parent.SelectSingleNode($name)
    if (-not $node) {
        $doc = $parent.OwnerDocument
        $node = $doc.CreateElement($name)
        $parent.AppendChild($node) | Out-Null
    }
    return $node
}
