function CheckInstall{
  
    #check if PsDrive exists if not we create the drive
    if (-not (Get-PSDrive -Name "HKCR" -ErrorAction SilentlyContinue)) {
        New-PSDrive -Name "HKCR" -PSProvider Registry -Root "HKEY_CLASSES_ROOT" | Out-Null
    }
    # Check if already installed in the correct path
    $currentPath = [System.Diagnostics.Process]::GetCurrentProcess().MainModule.FileName
    $targetExePath = Join-Path $ProgramPath "MKLinkCreator.exe"
    $outputIcoMain = Join-Path $ProgramPath "main.ico"
    $outputIcoSrc = Join-Path $ProgramPath "ICO_Source_V1.ico"
    $outputIcoDest = Join-Path $ProgramPath "ICO_Destination_V1.ico"

    #trigger installer
    if ($currentPath -ne $targetExePath) {
        <#
            first check if installed
            check if registry keys exsists and if exe file is located where it should be. 
            Also check if version of exe file is same as the running one. 
            IF version is the same or lower : show msg box with options: reinstall and uninstall and cancel
            IF version is higher: show msg box with options: upgrade and uninstall and cancel
        #>

        Write-Host "Installing MKLinkCreator to $ProgramPath..."



        # Create target directory
        if (-not (Test-Path $ProgramPath)) {
            New-Item -ItemType Directory -Path $ProgramPath -Force | Out-Null
        }

        # Copy EXE and icon
        Copy-Item -Path $currentPath -Destination $targetExePath -Force

        [IO.File]::WriteAllBytes($outputIcoSrc, [Convert]::FromBase64String($icoSource))
        [IO.File]::WriteAllBytes($outputIcoDest, [Convert]::FromBase64String($icoDest))
        [IO.File]::WriteAllBytes($outputIcoMain, [Convert]::FromBase64String($icoMain))
        # Define base registry paths
        $dirBase = 'Registry::HKCR\Directory\shell\MyTools'
        $dirBackBase = 'Registry::HKCR\Directory\Background\shell\MyTools'

        #Commands Location
        $CommandNamespace = "PolosAutomation.MyToolsCommands"
        $CommandsBase = "Registry::HKCR\$CommandNamespace"
        $cmd1 = "$CommandsBase\shell\MKLink_Source"
        $cmd2 = "$CommandsBase\shell\MKLink_Destination"
        $cmd3 = "$CommandsBase\shell\MKLink_Clear"


        #$base = "Registry::HKEY_CLASSES_ROOT\PolosAutomation.MyToolsCommands"
        #Set-ItemProperty -Path "Registry::HKEY_CLASSES_ROOT\Directory\shell\MyTools" -Name "ExtendedSubCommandsKey" -Value "PolosAutomation.MyToolsCommands"

