            New-Item -Path $var -Force | Out-Null
            #Set-ItemProperty -Path $var -Name "(default)" -Value "Polos Automation Command Base" -Force # dont use this, commands will not pop up if we have text here
        }

        # this is for use with  "ExtendedSubCommandsKey"
        # check if commands exsists in command base, else add them.
        # Source right-click option
        IF(!(Test-Path $cmd1)){
            $var = $cmd1
            New-Item -Path $var -Force | Out-Null
            Set-ItemProperty -Path $var -Name "(default)" -Value "Set MKLink Source"
            Set-ItemProperty -Path $var -Name "Icon" -Value "`"$outputIcoSrc`""
            New-Item -Path "$var\command" -Force | Out-Null
            Set-ItemProperty -Path "$var\command" -Name "(default)" -Value "`"$targetExePath`" -setSource `"%V`" -createLink"
        }

        # Destination right-click option
        IF(!(Test-Path $cmd2)){
            $var = $cmd2
            New-Item -Path $var -Force | Out-Null
            Set-ItemProperty -Path $var -Name "(default)" -Value "Set MKLink Destination"
            Set-ItemProperty -Path $var -Name "Icon" -Value "`"$outputIcoDest`""
            New-Item -Path "$var\command" -Force | Out-Null
            Set-ItemProperty -Path "$var\command" -Name "(default)" -Value "`"$targetExePath`" -setDestination `"%V`" -createLink"
        }

        # add "Clear MKLink Setup"
        IF(!(Test-Path $cmd3)){
            $var = $cmd3
            New-Item -Path $var -Force | Out-Null
            Set-ItemProperty -Path $var -Name "(default)" -Value "Clear MKLink Setup"
            Set-ItemProperty -Path $var -Name "Icon" -Value "`"$outputIcoMain`""
            New-Item -Path "$var\command" -Force | Out-Null
            Set-ItemProperty -Path "$var\command" -Name "(default)" -Value "`"$targetExePath`" -clearTempSetup"
        }

        # Create Cascading menu for folder right-click
        IF(!(Test-Path $dirBase)){
            $var = $dirBase 
            New-Item -Path $var -Force | Out-Null
            Set-ItemProperty -Path $var -Name "MUIVerb" -Value "My Tools"
            Set-ItemProperty -Path $var -Name "Icon" -Value "cmd.exe"
            Set-ItemProperty -Path $var -Name "ExtendedSubCommandsKey" -Value "$CommandNamespace" #this creates a link to the command space
            Set-ItemProperty -Path $var -Name "SubCommands" -Value ""
            Set-ItemProperty -Path $var -Name "Position" -Value "Top"

