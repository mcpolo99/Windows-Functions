function make-Link([string]$src, [string]$dst){
    Log "src: $src"
    Log "dst: $dst"
    
    if ((Test-Path $src -PathType Container) -and (Test-Path $dst -PathType Container)) {
        # Get the last directory name from the source path
        $sourceFolderName = Split-Path $src -Leaf 
        $targetLinkPath = Join-Path $dst "${sourceFolderName}_Link"

        Log "Source Path: $src"
        Log "Destination Path: $dst"
        Log "Source Folder Name: $sourceFolderName"

        try {
            if (Test-Path $targetLinkPath) {
                Remove-Item $targetLinkPath -Force
            }

            New-Item -ItemType SymbolicLink -Path $targetLinkPath -Target $src -ErrorAction Stop | Out-Null

            [System.Windows.Forms.MessageBox]::Show("Link created at:`n$targetLinkPath", "Success", "OK", "Information")

            #clean the cached folder locations
            ClearTemp

        }
        catch {
            Log "`n$($_.Exception.Message)"
            [System.Windows.Forms.MessageBox]::Show("Failed to create symbolic link:`n$($_.Exception.Message)", "Error", "OK", "Error")
        }

        if ($verbose) { Stop-Transcript }
        exit
    }
}

